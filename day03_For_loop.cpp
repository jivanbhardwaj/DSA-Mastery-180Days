#include <bits/stdc++.h>
using namespace std;

int main() {
    string name;
    getline(cin, name); // Read a line of input into the variable 'name'
// Using a for loop to print the number 10 times
// The loop runs from 1 to 10, printing the value of num each time
// This demonstrates the basic structure of a for loop in C++   
// The loop variable i starts at 1 and increments by 1 until it reaches 10  
//each iteration prints the value of num
// The loop will print the value of num 10 times
// This is a simple example of using a for loop to repeat an action
// The loop will execute 10 times, printing the value of num each time
    
    for(int i=1;i<=10;i++){
        cout <<name<<endl;
    }
    return 0;
}